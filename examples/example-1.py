# 列表 (存储多个元素, 列表中的元素可以重新赋值, 最外围用[]中括号表示)
# 列表可以直接通过下标取值,下标是从0开始编号,0就表示第一个值,不管是数组、还是列表、元组都会从0开始编号
# 取第1个值的方式为list[0]、第2个为b[1]、依次类推。。。
# 还可以修改列表中某个元素的值,如想修改8的值为100,那么可以使用 b[1] = 100
b = [7, 8, "abc", 9]
#print(b[0])
b[1] = 100
#print(b[1])

# 元组 (存储多个元素, 元组中的元素不可以重新赋值, 最外围用()小括号表示)
c = (7, 8, "abc", 9)
#print(c)

# 字典 {键: 值, 键: 值, ...},
# 取值格式: 字典名["对应的键名"]
