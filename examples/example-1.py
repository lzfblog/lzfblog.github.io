#print(d["job"])

# 集合 (用的不多,最好的用处就是去重,)
# 去重 (比如下面的例子中a比较多,使用集合之后,重复的a都会过滤掉只保留一个)
e1 = set("abcsdfsfaaaaa")
#print(e1)
e2 = set("abcdfg")
#print(e2)

# 交集
r1 = e1 & e2
#print(r1)

# 并集
r2 = e1 | e2
#print(r2)

# 差集
r3 = e1 - e2
#print(r3)

# 对称差集
r4 = e1 ^ e2
#print(r4)


